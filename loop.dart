void main() {
  // For loop: prints numbers from 1 to 10
  print("For Loop:");
  for (int i = 1; i <= 10; i++) {
    print(i);
  }

  // While loop: prints numbers from 10 to 1
  print("\nWhile Loop:");
  int j = 10;
  while (j >= 1) {
    print(j);
    j--;
  }

  // Do-while loop: prints numbers from 1 to 5
  print("\nDo-While Loop:");
  int k = 1;
  do {
    print(k);
    k++;
  } while (k <= 5);
}
