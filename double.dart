void main() {
  double num1 = 20.5;
  double num2 = 15.7;

  print(num1);
  print(num2);

  //sum two numbers
  double sum = num1 + num2;

  //minus two numbers
  double difference = num1 - num2;

  print(sum);
  print(difference);

  //multiplication
  double multiplication = num1 * num2;
  print(multiplication);
}
